                var email = GetEmailAddress(name);
                var mobile = "07" + DateTime.UtcNow.Ticks.ToString().Substring(8);

                var user = new User
                {
                    Id = i.ToString(),
                    Name = name,
                    Email = email,
                    Mobile = mobile
                };

                results.Add(user);
            }

            results.Add(new User { Id = "test", Name = "testdata", Email = "testdata@test.com", Mobile = "0712345678" });

            return results;
        }

